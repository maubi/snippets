public class Encap {
	private String anime;
	private String manga;
	private String novel;

	public Encap() {
	}
	public Encap(String a, String m, String n) {
		anime = a;
		manga = m;
		novel = n;
	}
	public String getAnime() {
		return anime;
	}
	public String getManga() {
		return manga;
	}
	public String getNovel() {
		return novel;
	}
	public void setAnime(String val) {
		anime = val;
	}
	public void setManga(String val) {
		manga = val;
	}
	public void setNovel(String val) {
		novel = val;
	}
}








